#!/usr/bin/env python3
"""floor function"""
import math


def floor(n: float) -> int:
    """
    Return the floor of a float.

    This function takes a float argument and returns the largest integer
    less than or equal to the given float.

    Parameters:
    n (float): The float whose floor value is to be calculated.

    Returns:
    int: The floor value of the float.
    """
    return math.floor(n)
