#!/usr/bin/env python3
"""add function"""


def add(a: float, b: float) -> float:
    """
    Add two floats.

    This function takes two float arguments and returns their sum.

    Parameters:
    a (float): The first float to be added.
    b (float): The second float to be added.

    Returns:
    float: The sum of the two floats.
    """
    return a + b
